import java.util.*;
public class M3Q6{
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        String n = scn.next();
        String[] array = n.split("");
        for(int i=0;i<array.length-1;i++){
            System.out.print(array[i] + " ");
        }
        System.out.println(array[array.length-1]);
    }
}
